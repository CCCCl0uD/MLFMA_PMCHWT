void gaussPoints::selectGauss_Point(const double*& l1, const double*& l2, const double*& l3, const double*& weight, const int N_points) {
	switch (N_points) {
	case 1: l1 = l1_1; l2 = l1_2; l3 = l1_3; weight = weight_1; break;
	case 3: l1 = l3_1; l2 = l3_2; l3 = l3_3; weight = weight_3; break;
	case 4: l1 = l4_1; l2 = l4_2; l3 = l4_3; weight = weight_4; break;
	case 6: l1 = l6_1; l2 = l6_2; l3 = l6_3; weight = weight_6; break;
	case 7: l1 = l7_1; l2 = l7_2; l3 = l7_3; weight = weight_7; break;
	case 12: l1 = l12_1; l2 = l12_2; l3 = l12_3; weight = weight_12; break;
	case 13: l1 = l13_1; l2 = l13_2; l3 = l13_3; weight = weight_13; break;
	case 16: l1 = l16_1; l2 = l16_2; l3 = l16_3; weight = weight_16; break;
	default:
		std::cerr << "Error: Invalid N_points for Gauss-Legendre integration: " << N_points << std::endl;
		throw std::invalid_argument("Unsupported Gauss point count: " + std::to_string(N_points));
		break;
	}
}