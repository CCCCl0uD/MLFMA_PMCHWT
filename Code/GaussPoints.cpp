/*
* Points.cpp 中的高斯积分点参考的是 D.A.DUNAVANT 与 1985 年发表的
* "HIGH DEGREE EFFICIENT SYMMETRICAL GAUSSIAN QUADRATURE RULES FOR THE TRIANGLE"
* 下载链接: https://www2.karlin.mff.cuni.cz/~knobloch/FILES/MKP_20_21/g_quadr.pdf
* weight | alpha | beta | gamma
* 判断规则：
* 1. 如果 alpha=beta=gamma=1/3 → 中心点 (1点)
* 2. 如果 beta=gamma≠alpha → 3点组 (a,b,b) 轮换展开为3点
* 3. 如果 alpha≠beta≠gamma≠ → 6点组 (a,b,c) 全排列展开为6点
* 3点组  (a, b, b) ==> | (a, b, b) | (b, a, b) | (b, b, a) |
* 6点组  (a, b, c) ==> | (a, b, c) | (a, c, b) | (b, a, c) | (b, c, a) | (c, a, b) | (c, b, a) |
* 前2点：a固定在第一位 → (a,b,c), (a,c,b)
* 中2点：b固定在第一位 → (b,a,c), (b,c,a)
* 后2点：c固定在第一位 → (c,a,b), (c,b,a)
*
* 注意：为方便计算，高斯积分点的权重缩小为 1/2
*/

